    private void cargarCombo()
    {
        ArrayList<Valoracion>lista=GE.llenarCombo();
        
        DefaultComboBoxModel modelo=new DefaultComboBoxModel();
        
        modelo.addElement("Seleccione");
        
        for (Valoracion V : lista) {
            
            modelo.addElement(V.getDetalle());
        }
        
        cboValor.setModel(modelo);
    }
    
    private void limpiarCampos()
    {
        txtNombre.setText("");
        cboValor.setSelectedIndex(-1);
        txtOpinion.setText("");
    }

