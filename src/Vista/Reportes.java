    private void cargarTabla()
    {
        ArrayList<Respuesta>lista=GE.Reporte3();
        
        DefaultTableModel modelo=new DefaultTableModel();
        
        String[]encabezado=new String[]{"ID", "Nombre", "Valoración"};
        
        modelo.setColumnIdentifiers(encabezado);
        
        for (Respuesta R : lista) {
            
            Object[]filas=new Object[]{R.getIdR(),R.getNombre(),R.getIdV()};
            
            modelo.addRow(filas);
        }
        tblReporte3.setModel(modelo);
    }

